part of 'theme.dart';

/// Static color constants used across all color schemes.
///
/// Provides raw named color values. Prefer using [AppColorScheme]
/// getters for semantic colors in widget code.
abstract final class Colors {
  static const Color white = Color(0xFFFFFFFF);
  static const Color gray = Color(0xFF808080);
  static const Color black = Color(0xFF000000);

  // Custom
  static const Color powderPetal = Color(0xFFffede1);
  static const Color lightCyan = Color(0xFFd7f9ff);
}
